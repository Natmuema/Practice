// Function to Find the Area of Rectangle
function areaRectangle(a, b) {
    let area = a * b;
    return area;
}

// Function to Find the Perimeter of Rectangle
function perimeterRectangle(a, b) {
    let perimeter = 2 * (a + b);
    return perimeter;
}


let a = 5;
let b = 6;
console.log("Area = " + areaRectangle(a, b));
console.log("Perimeter = " + perimeterRectangle(a, b)); 